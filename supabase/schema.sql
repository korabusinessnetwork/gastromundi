-- ── mesas (layout físico; status derivado no front) ───────────
CREATE TABLE public.mesas (
  numero        text PRIMARY KEY,
  capacidade    integer DEFAULT 4,
  posicao_x     integer,
  posicao_y     integer,
  status_manual text DEFAULT 'livre',
  created_at    timestamptz DEFAULT now(),
