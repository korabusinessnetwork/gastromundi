CREATE TABLE public.notas_fiscais_itens (
  id                 uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  nota_fiscal_id     uuid   REFERENCES public.notas_fiscais(id) ON DELETE CASCADE,
