CREATE TABLE public.combos (
  id                uuid    PRIMARY KEY DEFAULT gen_random_uuid(),
  nome              text    NOT NULL,
  item_principal_id bigint  NOT NULL REFERENCES public.products(id) ON DELETE CASCADE,
  modo              text    NOT NULL DEFAULT 'combo',
